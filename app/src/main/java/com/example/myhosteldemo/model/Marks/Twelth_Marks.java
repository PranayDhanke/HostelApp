package com.example.myhosteldemo.model.Marks;

public class Twelth_Marks {
    float marks , percent ;
    String marksheet , allotment , aadhar ;
    long time ;

    public Twelth_Marks() {
    }

    public float getMarks() {
        return marks;
    }

    public void setMarks(float marks) {
        this.marks = marks;
    }

    public float getPercent() {
        return percent;
    }

    public void setPercent(float percent) {
        this.percent = percent;
    }

    public String getMarksheet() {
        return marksheet;
    }

    public void setMarksheet(String marksheet) {
        this.marksheet = marksheet;
    }

    public String getAllotment() {
        return allotment;
    }

    public void setAllotment(String allotment) {
        this.allotment = allotment;
    }

    public String getAadhar() {
        return aadhar;
    }

    public void setAadhar(String aadhar) {
        this.aadhar = aadhar;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }
}
